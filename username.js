import { createHash } from "crypto";

export async function generateAccountIdentifier(input) {
  const hash = createHash("sha256").update(input).digest();

  // Use 16-bit chunks (2 bytes each) for better distribution
  // This reduces modulo bias and uses more of the hash entropy

  // Adjective: bytes 0-1 (16 bits = 0-65535)
  const adjIndex = ((hash[0] << 8) | hash[1]) >>> 0;
  const adj = adjectives[adjIndex % adjectives.length];

  // Noun: bytes 2-3 (16 bits)
  const nounIndex = ((hash[2] << 8) | hash[3]) >>> 0;
  const noun = nouns[nounIndex % nouns.length];

  // Verb: bytes 4-5 (16 bits)
  const verbIndex = ((hash[4] << 8) | hash[5]) >>> 0;
  const verb = verbs[verbIndex % verbs.length];

  // Number: bytes 6-9 (32 bits) for much larger range
  const randomNum =
    ((hash[6] << 24) | (hash[7] << 16) | (hash[8] << 8) | hash[9]) >>> 0;
  const num = randomNum % 10000; // 0-9999 for 10x more combinations

  return `${adj}${noun}${verb}${num}`;
}
