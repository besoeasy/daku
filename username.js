import { sha256 as nobleSha256 } from "@noble/hashes/sha2";

export function generateAccountIdentifier(input) {
  // Convert input string to Uint8Array if it's a string
  const inputBytes = typeof input === 'string'
    ? new TextEncoder().encode(input)
    : input;
  const hash = nobleSha256(inputBytes);

  // Noun: bytes 0-1 (16 bits)
  const nounIndex = ((hash[0] << 8) | hash[1]) >>> 0;
  const noun = nouns[nounIndex % nouns.length];

  // Verb: bytes 2-3 (16 bits)
  const verbIndex = ((hash[2] << 8) | hash[3]) >>> 0;
  const verb = verbs[verbIndex % verbs.length];

  // Number: bytes 4-7 (32 bits) for much larger range
  const randomNum =
    ((hash[4] << 24) | (hash[5] << 16) | (hash[6] << 8) | hash[7]) >>> 0;
  const num = randomNum % 10000; // 0-9999 for 10x more combinations

  return `${noun}${verb}${num}`;
}
